#include "../../minishell.h"

void ft_pwd(void)
{
	int i;
	char pwd[2048];

	getcwd(pwd, 2048);
	i = -1;
	while (pwd[++i])
		write(1, &pwd[i], 1);
	write(1, "\n", 1);
}
